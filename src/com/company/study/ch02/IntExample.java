package com.company.study.ch02;

public class IntExample {         //파일명과 class명은 동일해야함
								  //각 단어 첫글자는 대문자로(카멜표기법)
	public static void main(String[] args) {
		
		int var1 = 10;   //십진수
		int var2 = 012;  //8진수
		int var3 = 0xa;  //16진수
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);

	}

}
